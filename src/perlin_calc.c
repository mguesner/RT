double			lissage(double x, double y, double s[4])
{
	double	tmp;
	double	l[2];
	double	c[2];

	tmp = x - (int)x;
	c[0] = 3 * tmp * tmp - 2 * tmp * tmp * tmp;
	l[0] = s[0] + c[0] * (s[1] - s[0]);
	l[1] = s[2] + c[0] * (s[3] - s[2]);
	tmp = y - (int)y;
	c[1] = 3 * tmp * tmp - 2 * tmp * tmp * tmp;
	return (l[0] + c[1] * (l[1] - l[0]));
}

double			getperl(double x, double y, double res)
{
	int				i[4];
	double			s[4];
	const double	g[9][2] = { {1.0f / sqrt(2), 1.0f / sqrt(2)},
	{-1.0f / sqrt(2), 1.0f / sqrt(2)}, {1.0f / sqrt(2), -1.0f / sqrt(2)},
	{-1.0f / sqrt(2), -1.0f / sqrt(2)}, {1, 0}, {-1, 0}, {0, 1}, {0, -1}};

	x /= res;
	y /= res;
	i[0] = g_perm[((int)x & 255) + g_perm[((int)y & 255)]] % 8;
	i[1] = g_perm[((int)x & 255) + 1 + g_perm[((int)y & 255)]] % 8;
	i[2] = g_perm[((int)x & 255) + g_perm[((int)y & 255) + 1]] % 8;
	i[3] = g_perm[((int)x & 255) + 1 + g_perm[((int)y & 255) + 1]] % 8;
	s[0] = g[i[0]][0] * (x - (int)x) + g[i[0]][1] * (y - (int)y);
	s[1] = g[i[1]][0] * (x - ((int)x + 1)) + g[i[1]][1] * (y - (int)y);
	s[2] = g[i[2]][0] * (x - (int)x) + g[i[2]][1] * (y - ((int)y + 1));
	s[3] = g[i[3]][0] * (x - ((int)x + 1)) + g[i[3]][1] * (y - ((int)y + 1));
	return (lissage(x, y, s));
}
